-- 10.	List the product having the maximum monthly average number of transactions (consider the last 6 months data)
Select top 1 PRODUCTNAME from
	(
	Select PRODUCTNAME, AVG(Nooftxns) AvgMonthlyTxns from
		(
			Select PRODUCTNAME, datename(MM,DOT)as Month,COUNT(*) as Nooftxns from AMASTER am
			join
			PMASTER pm
			on am.PID = pm.PID
			join
			TMASTER tm
			on am.ACID = tm.ACID
			where DOT >= DATEADD(MM,-6,DOT) AND YEAR(DOT) = (SELECT YEAR(GETDATE()))
			group by productname, datename(MM,DOT)
		) as k
	group by PRODUCTNAME
	) as j
order by AvgMonthlyTxns desc
go

