	(
	select top 1 brid from AMASTER
	order by CBAL desc
	),
rankno as
	(
	select name, brid, cbal, DENSE_RANK() over (partition by brid order by cbal desc) as rankno
	from AMASTER
	where BRID = (select BRID from top1brid)
)
select name, cbal from rankno
where rankno = 2
GO

