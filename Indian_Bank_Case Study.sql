-- 6.	List the name of the account holder who has the second-highest cleared balance in the branch having 
--      the account with the maximum cleared balance.

-- Using Sub-queries
SELECT NAME,CBAL FROM AMASTER
WHERE CBAL = (SELECT MAX(CBAL) FROM AMASTER 
				WHERE CBAL < 
				(SELECT MAX(CBAL) FROM AMASTER)
				) 
			AND 
		BRID = 
			(
				SELECT TOP 1 BRID FROM AMASTER
				ORDER BY CBAL DESC
			)
GO

