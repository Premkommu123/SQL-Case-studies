-- 2.	Give the branch-wise total cash deposits that have taken place during the last 5 days
SELECT BRID, COUNT(*) AS TOTCASHDEPOSITS 
FROM TMASTER
WHERE TXN_TYPE = 'CD' AND DATEDIFF(DD,DOT, GETDATE()) <= 30
GROUP BY BRID
GO

/**********************************************************************************************/
-- 3.	Give the branch-wise total cash withdrawals during the last month, where the total cash withdrawals 
-- are greater than Rs 1,00,000
SELECT BRID, SUM(TXN_AMOUNT) AS TOTCASHWITHDRAWALS
FROM TMASTER
WHERE TXN_TYPE = 'CW' AND DATEDIFF(MM,DOT, GETDATE()) = 1
GROUP BY BRID
HAVING SUM(TXN_AMOUNT) > 10000
GO

