-- 13.	List the supplier name, part name and supplied quantity for all supplies made between 1st and 15th of June 2020.
with my_cte as
(
	Select * from SupplyDetails
	where DATEPART(dd,DATEOFSUPPLY) >= 1 and DATEPART(dd,DATEOFSUPPLY) <= 15 
	and DATEPART(MM,DATEOFSUPPLY) = 6 
	and DATEPART(YY,DATEOFSUPPLY) = 2025
)
select sm.NAME,pm.NAME, SUM(qtysupplied) as totalqty from SupplierMaster sm
join
my_cte ct on sm.SID = ct.SID
join
PartMaster pm
on ct.PID = pm.PID
group by sm.NAME,pm.NAME
go

