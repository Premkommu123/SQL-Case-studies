-- 12.	List the category-wise number of parts; exclude those where the sum is > 100 and less than 500. 
-- List in the descending order of sum.
Select Category, count(*) as Noofparts, SUM(QTYONHAND) Totalqty from PartMaster
group by CATEGORY
having not (SUM(QTYONHAND) > 100 and SUM(QTYONHAND) < 500)
order by Totalqty desc
