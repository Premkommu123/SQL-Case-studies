-- 15.	For the part with the minimum price, List the latest supply details (Supplier Name, Part id, Date of supply, Quantity Supplied).
select top 1 Name, pid, dateofsupply, QTYSUPPLIED from SupplyDetails sd
join
SupplierMaster sm
on sd.SID = sm.SID
where PID = 
			(
				select top 1 pid 
				from PartMaster 
				where PRICE = 
				(select MIN(Price) from PartMaster)
			)
order by DATEOFSUPPLY desc
go
