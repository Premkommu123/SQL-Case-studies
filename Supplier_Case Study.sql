-- 1.	List the month-wise average supply of parts supplied for all parts. Provide the information only if the average is higher than 20.
SELECT 	DATENAME(MM,DATEOFSUPPLY) AS MONTH,
		DATEPART(MM,DATEOFSUPPLY) AS MONTHNUM,
		NAME,
		AVG(QTYSUPPLIED) AVGQTY 
FROM SupplyDetails S
JOIN PartMaster P
	ON S.PID = P.PID
GROUP BY NAME, DATENAME(MM,DATEOFSUPPLY),DATEPART(MM,DATEOFSUPPLY)
HAVING AVG(QTYSUPPLIED) > 20
ORDER BY DATEPART(MM,DATEOFSUPPLY) ASC
GO

-- 2.	List the names of the Suppliers who do not supply part with PID 1.
SELECT NAME FROM SupplierMaster sm
left join
SupplyDetails sd
on sd.SID = sm.SID and PID = 1
where sd.SID is null
GO

select Name from SupplierMaster SM 
