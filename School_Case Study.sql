-- 1.	List the course wise total no. of Students enrolled. 
--      Provide the information only for students of foreign origin and only if the total exceeds 10.
SELECT COURSENAME, Origin, COUNT(*) AS TOTALSTUDENTS
FROM CourseMaster CM
JOIN
EnrollmentMaster EM
ON CM.CID = EM.CID
JOIN StudentMaster SM
ON EM.SID = SM.SID
WHERE Origin = 'F'
GROUP BY CourseName,Origin
HAVING COUNT(*) > 10
go

-- 2.	List the names of the Students who have not enrolled for Artificial Intelligence Overview course.
SELECT STUDENTNAME 
FROM StudentMaster
WHERE SID not in
		(
			SELECT SID FROM EnrollmentMaster WHERE CID <> 120
			)
go

-- 3.	List the name of the advanced course where the enrollment by foreign students is the highest.	
SELECT TOP 1 CourseName,COUNT(*) AS NOOFENROLL FROM CourseMaster CM
	JOIN EnrollmentMaster EM ON EM.CID = CM.CID
	JOIN StudentMaster SM ON SM.SID = EM.SID
WHERE Category = 'A' AND Origin = 'F'
GROUP BY CourseName
ORDER BY NOOFENROLL DESC
GO

-- 4.	List the names of the students who have enrolled for at least one basic course in the current month.
SELECT DISTINCT StudentName 
FROM StudentMaster SM
	JOIN EnrollmentMaster EM ON SM.SID = EM.SID
	JOIN CourseMaster CM ON CM.CID = EM.CID
WHERE Category = 'A' AND DATEDIFF(MM,DOE,GETDATE()) = 0
go

-- 5.	List the names of the Undergraduate, local students who have got a “C” grade in any basic course.
SELECT DISTINCT StudentName
FROM StudentMaster SM
	JOIN EnrollmentMaster EM ON SM.SID = EM.SID
	JOIN CourseMaster CM ON CM.CID = EM.CID
WHERE Type = 'U' 
	AND Origin = 'L' 
	AND Grade = 'C' 
	AND Category = 'B'
go

-- 6.	List the names of the courses for which no student has enrolled in the month of May 2025.
SELECT COURSENAME FROM CourseMaster
WHERE CID NOT IN
	(
	SELECT CM.CID
	FROM StudentMaster SM
		JOIN EnrollmentMaster EM ON SM.SID = EM.SID
		JOIN CourseMaster CM ON CM.CID = EM.CID
		where MONTH(DOE) = 5 AND YEAR(DOE) = 2025
	)
go

