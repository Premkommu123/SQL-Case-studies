-- 7.	List name, Number of Enrollments and Popularity for all Courses. 
--      Popularity has to be displayed as “High” if number of enrollments is higher than 50,  “Medium” if greater than or equal to 20 and less than 50, and “Low” if the no.  Is less than 20.
SELECT CourseName, 
		COUNT(*) AS NOOFENROLS,
		CASE
			WHEN COUNT(*) > 55 THEN 'HIGH'
			WHEN COUNT(*) >= 45 AND COUNT(*) <= 55 THEN 'MEDIUM'
			ELSE
			'LOW'
		END AS POPULARITY
FROM CourseMaster cm
LEFT JOIN EnrollmentMaster e
    ON cm.CID = e.CID
GROUP BY CourseName
ORDER BY NOOFENROLS DESC
GO

-- 8.	List the most recent enrollment details with information on Student Name, Course name and age of enrollment in days.
SELECT TOP 1 StudentName, CourseName, DATEDIFF(DD,DOE,'2025-12-31') AS NOOFDAYS
FROM StudentMaster SM
	JOIN EnrollmentMaster EM ON SM.SID = EM.SID
	JOIN CourseMaster CM ON CM.CID = EM.CID
ORDER BY NOOFDAYS ASC
GO

